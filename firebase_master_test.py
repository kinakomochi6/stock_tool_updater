def get_valid_ticker_and_price(code):
    suffixes = ['.T', '.S', '.F', '.N'] 
    for suffix in suffixes:
        symbol = f"{code}{suffix}"
        try:
            ticker = yf.Ticker(symbol)
            try:
                info = ticker.info
                if info and info.get('currentPrice'):
                    return ticker, info['currentPrice']
            except: pass
            hist = ticker.history(period="1d")
            if not hist.empty:
                return ticker, hist['Close'].iloc[-1]
        except: continue
    return None, 0

def scrape_fallback_data(code):
    import re, requests
    from bs4 import BeautifulSoup
    headers = {'User-Agent': 'Mozilla/5.0'}
    
    data = {
        "price": 0, "mcap_oku": 0, "div_yield": 0, "per": 0,
        "pbr": 0, "eps": 0, "roe": 0, "bps": 0
    }
    
    # 1. Minkabu (株価・時価総額)
    try:
        url_mk = f"https://minkabu.jp/stock/{code}"
        res_mk = requests.get(url_mk, headers=headers, timeout=5)
        if res_mk.status_code == 200:
            soup_mk = BeautifulSoup(res_mk.text, 'html.parser')
            pr_elem = soup_mk.find('div', class_='stock_price')
            if pr_elem:
                p_str = re.sub(r'[^\d.]', '', pr_elem.text.strip())
                if p_str: data["price"] = float(p_str)
            mcap_th = soup_mk.find(string=re.compile("時価総額"))
            if mcap_th:
                mcap_td = mcap_th.find_next('td')
                if mcap_td:
                    m_str = re.sub(r'[^\d.]', '', mcap_td.text)
                    if m_str: data["mcap_oku"] = float(m_str) / 100
    except: pass

    # 2. Yahoo Finance Japan (各種指標)
    def extract_num(text, keyword):
        if keyword not in text: return 0
        idx = text.find(keyword) + len(keyword)
        sub = text[idx:].replace('(連)', '').replace('(単)', '').replace(' ', '')
        m = re.search(r'[\d,]+\.?\d*', sub)
        if m:
            try: return float(m.group(0).replace(',', ''))
            except: return 0
        return 0

    try:
        suffixes = ['.N', '.S', '.F']
        soup_yj = None
        for suf in suffixes:
            url_yj = f"https://finance.yahoo.co.jp/quote/{code}{suf}"
            res_yj = requests.get(url_yj, headers=headers, timeout=5)
            # Yahoo Japan Financeは銘柄がなくても200を返し、「一致する銘柄は見つかりませんでした」等になる場合がある
            soup_temp = BeautifulSoup(res_yj.text, 'html.parser')
            if soup_temp.find(string=re.compile("時価総額")):
                soup_yj = soup_temp
                break
        
        if soup_yj:
            for li in soup_yj.find_all('li'):
                t = li.text.strip().replace('\n', '')
                if '配当利回り' in t and data['div_yield'] == 0: data['div_yield'] = extract_num(t, '配当利回り')
                elif 'PER' in t and ('会社予想' in t or '予想' in t) and data['per'] == 0: data['per'] = extract_num(t, 'PER')
                elif 'PBR' in t and data['pbr'] == 0: data['pbr'] = extract_num(t, 'PBR')
                elif 'EPS' in t and data['eps'] == 0: data['eps'] = extract_num(t, 'EPS')
                elif 'BPS' in t and data['bps'] == 0: data['bps'] = extract_num(t, 'BPS')
                elif 'ROE' in t and data['roe'] == 0: data['roe'] = extract_num(t, 'ROE')
    except: pass

    return data

