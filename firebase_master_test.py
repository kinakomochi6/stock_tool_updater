def analyze_bs_xbrl(doc_id):
    url = f"https://disclosure.edinet-fsa.go.jp/api/v2/documents/{doc_id}"
    params = {"type": 1, "Subscription-Key": EDINET_API_KEY}
    try: 
        res = requests.get(url, params=params, timeout=10)
        if res.status_code != 200: return None
        z_data = io.BytesIO(res.content)
    except: return None
    
    summary = {k: 0 for k in DISPLAY_ORDER}
    totals = {"Assets":0, "CurrentAssets":0, "NonCurrentAssets":0, "Liabilities":0, "CurrentLiabilities":0, "NonCurrentLiabilities":0, "NetAssets":0}

    with zipfile.ZipFile(z_data) as z:
        xbrl_file = next((n for n in z.namelist() if n.endswith(".xbrl") and "PublicDoc" in n), None)
        if not xbrl_file: return None
        
        with z.open(xbrl_file) as f:
            try: soup = BeautifulSoup(f, 'lxml-xml')
            except: soup = BeautifulSoup(f, 'html.parser')

            if soup.find(re.compile("ifrs", re.IGNORECASE)): return "IFRS"
            
            dei = soup.find(re.compile(r'.*CurrentPeriodEndDate'))
            end_date = dei.text.strip() if dei else None
            
            # ★ここが6396問題を解決した、確実に動いていたコードです
            valid_ctx_candidates = []
            for ctx in soup.find_all(re.compile(r'.*context')):
                cid = ctx.get('id')
                instant = ctx.find(re.compile(r'.*instant'))
                if not instant: continue
                if end_date and instant.text.strip() != end_date: continue
                
                members = ctx.find_all(re.compile(r'.*explicitMember'))
                is_valid_main_ctx = True
                if len(members) > 0:
                    for m in members:
                        m_text = m.text
                        if "ConsolidatedMember" not in m_text and "NonConsolidatedMember" not in m_text:
                            is_valid_main_ctx = False
                            break
                if is_valid_main_ctx:
                    valid_ctx_candidates.append(cid)
            
            # 各候補で実際に何個タグが取れるか試してみて、最もヒット数が多いものを採用する
            # ※高速化のため、すべての要素を一度だけ探索して contextRef ごとにまとめる
            elements_by_ctx = {}
            for el in soup.find_all():
                ctx_ref = el.get('contextRef')
                if ctx_ref:
                    elements_by_ctx.setdefault(ctx_ref, []).append(el)

            max_hits = -1
            best_res = (summary, totals)
            best_cid = None
            
            for cid in valid_ctx_candidates:
                temp_summary = {k: 0 for k in DISPLAY_ORDER}
                temp_totals = {k: 0 for k in totals}
                hits = 0
                
                raw_tags = {}
                elements = elements_by_ctx.get(cid, [])
                for el in elements:
                    text_str = el.text.strip()
                    if not re.match(r'^-?[\d\.]+$', text_str): continue
                    try: val = int(float(text_str))
                    except: continue
                    tag = el.name.split(':')[-1]
                    # 後に出現した値を優先（または同一コンテキストなら同じ値）
                    raw_tags[tag] = val
                    if tag in temp_totals: temp_totals[tag] = val
                
                for tag, val in raw_tags.items():
                    if tag in TAG_MAPPING:
                        # 棚卸資産の内訳と合計の二重計上防止
                        if tag in ["MerchandiseAndFinishedGoods", "WorkInProcess", "RawMaterialsAndSupplies", "RealEstateForSale", "RealEstateForSaleInProcess", "CostsOnRealEstateBusiness", "CostsOnUncompletedConstructionContracts"]:
                            if "Inventories" in raw_tags:
                                continue # 合計タグであるInventoriesが存在する場合は内訳を加算しない
                        
                        # 固定資産のGross/Net二重計上防止
                        if not tag.endswith("Net") and (tag + "Net") in raw_tags:
                            continue # Netタグが存在する場合はGross金額を加算しない
                            
                        temp_summary[TAG_MAPPING[tag]] += val
                        hits += 1
                
                if hits > max_hits:
                    max_hits = hits
                    best_cid = cid
                    best_res = (temp_summary, temp_totals)
                elif hits == max_hits and max_hits > 0:
                    # 同じヒット数なら、NonConsolidated という文字が入っていないほう（連結っぽいほう）を優先
                    if "NonConsolidated" not in cid and (best_cid and "NonConsolidated" in best_cid):
                         best_cid = cid
                         best_res = (temp_summary, temp_totals)
            
            summary, totals = best_res
            valid_ctx = {best_cid} if best_cid else set()

    if summary["流動_受取手形"] > 0 or summary["流動_売掛金"] > 0:
        summary["流動_受取手形・売掛金(合算)"] = 0

    if totals["NonCurrentAssets"] == 0 and totals["Assets"] != 0:
        totals["NonCurrentAssets"] = totals["Assets"] - totals["CurrentAssets"]
    if totals["NonCurrentLiabilities"] == 0 and totals["Liabilities"] != 0:
        totals["NonCurrentLiabilities"] = totals["Liabilities"] - totals["CurrentLiabilities"]

    def calc_gap(p, t, o):
        s = sum(summary[k] for k in summary if k.startswith(p) and k != o)
        summary[o] = (totals[t] - s)
    
    calc_gap("流動_", "CurrentAssets", "流動_その他流動資産")
    sum_fixed = sum(summary[k] for k in summary if (k.startswith("有形_") or k.startswith("無形_") or k.startswith("投資_")) and k != "投資_その他固定資産")
    summary["投資_その他固定資産"] = (totals["NonCurrentAssets"] - sum_fixed)
    calc_gap("流負_", "CurrentLiabilities", "流負_その他流動負債")
    calc_gap("固負_", "NonCurrentLiabilities", "固負_その他固定負債")
    sum_net = sum(summary[k] for k in summary if k.startswith("純資_") and k != "純資_その他純資産")
    summary["純資_その他純資産"] = (totals["NetAssets"] - sum_net)
    
    return summary, totals


# --- 解析用ヘルパー関数群 ---
def normalize_str(obj):
    if obj is None: return ""
    return unicodedata.normalize('NFKC', str(obj)).replace(' ', '').replace('\n', '').replace('\u3000', '')

def get_valid_number(text):
    if pd.isna(text) or text is None: return None
    text_str = str(text)
    
    # 改行や空白で区切ってブロックごとに評価 (1869のようなHTML重複対策)
    blocks = re.split(r'[\n\s\u3000]+', text_str.strip())
    
    for block in blocks:
        normalized = unicodedata.normalize('NFKC', block).replace('△', '-').replace('▲', '-')
        normalized = re.sub(r'※\d+', '', normalized)
        normalized = re.sub(r'\(注\d*\)', '', normalized)
        
        clean_text = re.sub(r'[^\d\.\-]', '', normalized)
        try:
            if clean_text and re.fullmatch(r'-?\d+(\.\d+)?', clean_text):
                val = float(clean_text)
                if abs(val) < 10 and val.is_integer(): continue
                return val
        except: pass

    return None

def detect_unit(text):
    text = normalize_str(text)
    if "単位:百万円" in text or "単位：百万円" in text or "(百万円)" in text or "（百万円）" in text: return 1000000
    if "単位:千円" in text or "単位：千円" in text or "(千円)" in text or "（千円）" in text: return 1000
    if "単位:円" in text or "単位：円" in text: return 1
    return 1000000 

def resolve_sum_or_max(values):
    if not values: return 0
    if len(values) == 1: return values[0]
    total_sum = sum(values)
    max_val = max(values)
    rest_sum = total_sum - max_val
    if rest_sum > 0 and abs(max_val - rest_sum) / max_val < 0.05: return max_val
    return total_sum

def analyze_single_table(tbl_soup, prev_text):
    unit = detect_unit(tbl_soup.get_text())
    if unit == 1000000: unit = detect_unit(prev_text)

    try:
        dfs = pd.read_html(io.StringIO(str(tbl_soup)), header=None)
        if not dfs: return 0, 0
        df = dfs[0]
    except: return 0, 0

    rows, cols = df.shape
    if rows < 2 or cols < 2: return 0, 0

    target_col_indices = []
    col_headers = ["".join([normalize_str(df.iloc[r, c]) for r in range(min(5, rows))]) for c in range(cols)]

    for c in range(cols):
        h_text = col_headers[c]
        if "当" in h_text and any(x in h_text for x in ["連結", "事業", "年度", "期"]):
            if "前期" not in h_text and "期首" not in h_text:
                target_col_indices.append(c)
    
    if not target_col_indices:
        for c in range(cols - 1, 0, -1):
            if any(x in col_headers[c] for x in ["前連結", "前事業", "前期", "増減"]): continue
            for r in range(rows):
                if get_valid_number(df.iloc[r, c]) is not None:
                    target_col_indices.append(c)
                    break 
            if target_col_indices: break

    if not target_col_indices: return 0, 0
    target_col = target_col_indices[-1]

    found_books, found_markets = [], []
    for r in range(rows):
        row_label = "".join([normalize_str(df.iloc[r, c]) for c in range(target_col)])
        val = get_valid_number(df.iloc[r, target_col])
        if val is None: continue

        is_book_row = False
        if "期末" in row_label and ("残高" in row_label or "計上額" in row_label): is_book_row = True
        elif "貸借対照表計上額" in row_label or "帳簿価額" in row_label: is_book_row = True

        if is_book_row and any(x in row_label for x in ["期首", "増減", "償却", "損益"]): is_book_row = False
        if is_book_row and "期首" not in row_label: found_books.append(val)
        if "時価" in row_label: found_markets.append(val)

    return resolve_sum_or_max(found_books) * unit, resolve_sum_or_max(found_markets) * unit

# ★【追加】有価証券の「取得原価を超えるもの」の差額を取得する関数
def analyze_securities_table(tbl_soup, prev_text):
    unit = detect_unit(tbl_soup.get_text())
    if unit == 1000000: unit = detect_unit(prev_text)

    try:
        dfs = pd.read_html(io.StringIO(str(tbl_soup)), header=None)
        if not dfs: return 0
        df = dfs[0]
    except: return 0

    rows, cols = df.shape
    if rows < 2 or cols < 2: return 0

    is_valid_table = False
    diff_col_idx = -1
    for r in range(rows):
        row_values = []
        for c in range(cols):
            v = get_valid_number(df.iloc[r, c])
            if v is not None: row_values.append((c, v))
        
        # 3つの値 A, B, C があり、 A - B = C が成り立つ組み合わせを探す
        for i in range(len(row_values)):
            for j in range(len(row_values)):
                for k in range(len(row_values)):
                    if i == j or j == k or i == k: continue
                    ci, vi = row_values[i]; cj, vj = row_values[j]; ck, vk = row_values[k]
                    if vk > 0 and abs(vi - vj - vk) < 5:
                        is_valid_table = True
                        diff_col_idx = ck
                        break
                if is_valid_table: break
            if is_valid_table: break
        if is_valid_table: break
    
    if not is_valid_table:
        diff_col_idx = -1
        for c in range(cols):
            col_text = "".join([normalize_str(df.iloc[r, c]) for r in range(min(5, rows))])
            if any(x in col_text for x in ["差額", "評価差額", "評価益", "含み損益", "z"]): 
                diff_col_idx = c
                break
        if diff_col_idx == -1:
            for c in range(cols):
                for r in range(min(10, rows)):
                    norm_val = normalize_str(df.iloc[r, c])
                    if any(x in norm_val for x in ["差額", "評価差額", "評価益", "含み損益", "z"]):
                        diff_col_idx = c
                        break
                if diff_col_idx != -1: break
    
    if diff_col_idx == -1: return 0
    if not is_valid_table and diff_col_idx <= 1: return 0

    found_values = []
    in_exceed_section = False

    for r in range(rows):
        row_label = "".join([normalize_str(df.iloc[r, c]) for c in range(diff_col_idx)])

        # 1行目はラベルにかかわらず「超えるもの」である可能性が高い（特に mojibake 時）
        is_matched = any(x in row_label for x in ["超える", "超過", "プラス"])
        if is_matched:
            in_exceed_section = True
        elif any(x in row_label for x in ["超えない", "以下", "合計", "マイナス"]):
            in_exceed_section = False  
        elif r == 0 and len(found_values) == 0: 
            # バックアップ：最初の数行でまだ何も見つかっていなければ、暫定的に True にしてみる
            in_exceed_section = True

        if in_exceed_section:
            val = get_valid_number(df.iloc[r, diff_col_idx])
            if val is not None and val > 0:
                found_values.append(val)
                # 一度値が見つかったら、次の行が合計や「超えない」なら in_exceed_section を False にする
                # ただし、3202のように1行しかない場合はそのまま終わる
    
    # 合計値が混ざっているのを防ぐため、resolve_sum_or_max で調整
    return resolve_sum_or_max(found_values) * unit

